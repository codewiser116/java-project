package day2;

public class ArithmeticOperators {

    public static void main (String [] args){

        int num1 = 2 + 3;
        System.out.println(num1);

        int num2 = num1 * 2;
        System.out.println(num2);

        System.out.println(2 + 10 - 4 * 5); // -8

        int num3 = 2 + (10 - 4) * 5;
        System.out.println(num3);

    }

}
