package day2;

public class Modulus {
    public static void main (String [] args){

        int a = 10;
        int b = 3;
        int c = a % b; // 1
        System.out.println(c);

        int d = 100;
        int e = 90;
        int f = d % e; // 10
        System.out.println(f);

        System.out.println(90 % 40); // 10

        System.out.println(60 % 40); // 20


        System.out.println(9 % 2);



    }
}
