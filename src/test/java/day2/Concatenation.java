package day2;

public class Concatenation {
    public static void main (String [] args){

        String name = "Codewise";
        System.out.println("hello " + name);

        int age = 2;
        System.out.println("Hello! I am " + age + " years old");

        int year = 2024;

        System.out.println(year - age); //2022
        System.out.println(year + age); //2026

        System.out.println("Today is: " + year + age); //Today is: 20242
        System.out.println(year + age + " is today's date"); //2026 is today's date







        System.out.println("It's " + 86 + 2 + " degrees outside"); //its 862
        System.out.println("It's " + (86 + 2) + " degrees outside");

        int a = 100;
        int b = 5;

        System.out.println("I'm " + a + b + " years old"); //I'm 1005
        System.out.println("I'm " + (a + b) + " years old"); //I'm 105


        System.out.println("Hello! " + 100 * 5 + " dollars!"); // hello 500
        System.out.println("Hello! " + 100 / 5 + " dollars!"); // hello 20
        System.out.println("Hello! " + (100 - 5) + " dollars!"); // hello 95


        int apple = 12;
        int banana = 2;

        System.out.println(apple + banana + " number of fruits"); //32 number
        //32 number of fruits

        System.out.println("I have so many fruits: " + apple + banana);
        // I have so many fruits 1220


        System.out.println(apple * banana + " fruits"); // 24 fruits
        System.out.println(3 + 12 * 2); //
        System.out.println("fd" + 12 * 2); // fd24

        int x = 3;
        int y = 2;
        int z = 5;

        System.out.println(x + " hello " + y * z); //3 hello 10

        System.out.println("hello " + x + y * z); //hello 310





    }
}
